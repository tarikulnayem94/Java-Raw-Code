package com.Practice;

public class If_else {
	public static void main(String[] args) {

		int A = 30;

		if (A == 30) // while(Boolean_Expression)
		{
			System.out.println("Number A=30");
		} else if (A < 30) {
			System.out.println("Number A<30");
		}

		else {
			System.out.println("Number A>30");
		}
	}
}
